namespace DevQualX.Web.Components.Library.Atoms;

/// <summary>
/// Notification severity levels for toast styling and icons.
/// </summary>
public enum ToastLevel
{
    /// <summary>
    /// Primary level - Blue color, informational.
    /// </summary>
    Primary,
    
    /// <summary>
    /// Secondary level - Gray color, neutral information.
    /// </summary>
    Secondary,
    
    /// <summary>
    /// Success level - Green color, successful operations.
    /// </summary>
    Success,
    
    /// <summary>
    /// Danger level - Red color, errors and critical issues.
    /// </summary>
    Danger,
    
    /// <summary>
    /// Warning level - Yellow/Orange color, warnings and cautions.
    /// </summary>
    Warning,
    
    /// <summary>
    /// Info level - Light blue color, informational messages.
    /// </summary>
    Info
}
