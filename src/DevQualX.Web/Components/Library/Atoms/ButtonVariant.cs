namespace DevQualX.Web.Components.Library.Atoms;

/// <summary>
/// Button style variants for the Button component.
/// </summary>
public enum ButtonVariant
{
    /// <summary>
    /// Default/secondary button style with border.
    /// </summary>
    Default,
    
    /// <summary>
    /// Primary action button style (filled background).
    /// </summary>
    Primary,
    
    /// <summary>
    /// Secondary button style (subtle background).
    /// </summary>
    Secondary,
    
    /// <summary>
    /// Danger/destructive action button style (red).
    /// </summary>
    Danger,
    
    /// <summary>
    /// Ghost button style (transparent background, only visible on hover).
    /// </summary>
    Ghost,
    
    /// <summary>
    /// Link button style (appears as a link).
    /// </summary>
    Link
}
